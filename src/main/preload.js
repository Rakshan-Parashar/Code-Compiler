const { contextBridge, ipcRenderer } = require('electron')
const on  = (ch, cb) => { ipcRenderer.on(ch, (_, d) => cb(d)); return () => ipcRenderer.removeAllListeners(ch) }
const inv = (ch, ...a) => ipcRenderer.invoke(ch, ...a)
const snd = (ch, ...a) => ipcRenderer.send(ch, ...a)
contextBridge.exposeInMainWorld('api', {
  winMin:       ()        => snd('win:minimize'),
  winMax:       ()        => snd('win:maximize'),
  winClose:     ()        => snd('win:close'),
  winFullscreen:()        => snd('win:fullscreen'),
  winIsMax:     ()        => inv('win:isMax'),
  onWinState:   cb        => on('win:state', cb),
  onFullscreen: cb        => on('win:fullscreen', cb),
  loadSettings: ()        => inv('settings:load'),
  saveSettings: s         => inv('settings:save', s),
  loadAccount:  ()        => inv('account:load'),
  saveAccount:  a         => inv('account:save', a),
  logout:       ()        => inv('account:logout'),
  accountSignup:(name, email, password) => inv('account:signup', { name, email, password }),
  accountLogin: (email, password) => inv('account:login', { email, password }),
  cloudList:    ()        => inv('cloud:list'),
  cloudSave:    s         => inv('cloud:save', s),
  cloudDelete:  id        => inv('cloud:delete', id),
  loadRecent:   ()        => inv('recent:load'),
  loadBookmarks:()        => inv('bookmarks:load'),
  saveBookmarks:b         => inv('bookmarks:save', b),
  historyPush:  (fp,c)    => inv('history:push', { fp, content: c }),
  historyUndo:  fp        => inv('history:undo', fp),
  openFile:     ()        => inv('fs:openFile'),
  openFolder:   ()        => inv('fs:openFolder'),
  readFile:     p         => inv('fs:readFile', p),
  saveFile:     (p,c)     => inv('fs:saveFile',   { filePath: p, content: c }),
  saveFileAs:   (c,n)     => inv('fs:saveFileAs', { content: c, defaultName: n }),
  newFile:      (dir,n)   => inv('fs:newFile',    { folderPath: dir, fileName: n }),
  newFolder:    (dir,n)   => inv('fs:newFolder',  { folderPath: dir, folderName: n }),
  deleteItem:   p         => inv('fs:delete', p),
  renameItem:   (old,n)   => inv('fs:rename',  { oldPath: old, newName: n }),
  refreshDir:   p         => inv('fs:refresh', p),
  revealItem:   p         => inv('fs:reveal',  p),
  clipboardWrite: t       => inv('clipboard:write', t),
  clipboardRead:  ()      => inv('clipboard:read'),
  gitStatus:    cwd       => inv('git:status',  cwd),
  gitBranch:    cwd       => inv('git:branch',  cwd),
  gitLog:       cwd       => inv('git:log',     cwd),
  gitDiff:      (cwd,f)   => inv('git:diff',    { cwd, file: f }),
  gitShowHead:  (cwd,f)   => inv('git:showHead', { cwd, file: f }),
  gitStage:     (cwd,f)   => inv('git:stage',   { cwd, file: f }),
  gitUnstage:   (cwd,f)   => inv('git:unstage', { cwd, file: f }),
  gitCommit:    (cwd,msg) => inv('git:commit',  { cwd, msg }),
  gitPush:      cwd       => inv('git:push',    cwd),
  gitPull:      cwd       => inv('git:pull',    cwd),
  gitDiscard:   (cwd,f)   => inv('git:discard', { cwd, file: f }),
  gitInit:      cwd       => inv('git:init',    cwd),
  runCode:      (code,lang) => inv('exec:run', { code, language: lang }),
  stopCode:     ()        => inv('exec:stop'),
  onExecOut:    cb        => on('exec:out',  cb),
  onExecDone:   cb        => on('exec:done', cb),
  aiChat:       (prompt, provider, apiKey, model) => inv('ai:chat', { prompt, provider, apiKey, model }),
  aiReview:     (code, language, provider, apiKey, model) => inv('ai:review', { code, language, provider, apiKey, model }),
  aiCancel:     ()        => inv('ai:cancel'),
  onAiProgress: cb        => on('ai:progress', cb),
  hasEnvKey:    ()        => inv('ai:hasEnvKey'),
  ptyCreate:    cwd       => inv('pty:create', { cwd }),
  ptyWrite:     d         => snd('pty:write',  d),
  ptyResize:    (c,r)     => snd('pty:resize', { cols: c, rows: r }),
  ptyKill:      ()        => inv('pty:kill'),
  onPtyData:    cb        => on('pty:data', cb),
  onPtyExit:    cb        => on('pty:exit', cb),
  lintJS:       code      => inv('lint:js',     code),
  formatCode:   (code,lang) => inv('format:code', { code, language: lang }),
  sysInfo:      ()        => inv('sys:info'),
  watchStart:   fp        => inv('watch:start', fp),
  watchStop:    fp        => inv('watch:stop',  fp),
  onFileChange: cb        => on('watch:change', cb),

  notebooksList:   (root) => inv('notebooks:list', root),
  notebooksCreate: (root, name) => inv('notebooks:create', { root, name }),
  notebooksRead:   (root, name) => inv('notebooks:read', { root, name }),
  notebooksWrite:  (root, name, content) => inv('notebooks:write', { root, name, content }),
  notebooksDelete: (root, name) => inv('notebooks:delete', { root, name }),
})